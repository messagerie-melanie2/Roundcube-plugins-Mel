function ScoreTmdp() {
  var hibp = document.getElementById('hibp');
  if (hibp) {
    hibp.checked = false;
  }

  var mdp = document.getElementById(HTML_PASSWORD).value;
  DebugLog('===== ScoreTmdp =====', mdp);
  var s = '',
    bits = 0,
    score = '';

  bits = Math.round(EvalTmdp(mdp, 0));
  DebugLog('====== Score ======', bits);

  if (bits < 24) {
    score = 'E';
  } else if (bits < 48) {
    score = 'D';
  } else if (bits < 64) {
    score = 'C';
  } else if (bits < 80) {
    score = 'B';
  } else {
    score = 'A';
  }

  SetComplexity(bits, GetColorFromScore(score));
  SetScore(score);
  if (MSG_ENTROPIE) {
    SetText('Entropie estimée&nbsp;: ' + bits + ' bits', score);
  } else {
    SetText(GetMessageFromScore(score), score);
  }
}

