// pour les moteurs Javascript comme IE qui ne connaissent pas Math.log2
Math.log2 = Math.log2 || function(x){return Math.log(x)*Math.LOG2E;};

const MinComponentsLength = 4
const MaxComponentsLength = 12
const BitsCommonWords = Math.log2(Object.keys(CommonWords).length);
const BitsChar = Math.log2(26);
const BitsNum = Math.log2(10);
const BitsAlt = Math.log2(38);
const BitsCharset = Math.log2(74);
const BitsNumID = Math.log2(10);
const BitsRepeatedChar = 1.0;

function GetFrequencesIndex(c)
{
    c = c.charAt(0).toLowerCase();
    if (c < 'a' || c > 'z')
    {
    return 0;
    }
    return c.charCodeAt(0) - 'a'.charCodeAt(0) + 1;
}

function FrenchLowerCase(s) {
    var r = s.toLowerCase();
    r = r.replace(/é/g, "e").replace(/è/g, "e").replace(/ê/g, "e").replace(/ë/g, "e");
    r = r.replace(/à/g, "a").replace(/â/g, "a").replace(/â/g, "a");
    r = r.replace(/ô/g, "o").replace(/ö/g, "o");
    r = r.replace(/ù/g, "u").replace(/û/g, "u").replace(/ü/g, "u");
    r = r.replace(/î/g, "i").replace(/ï/g, "i");
    r = r.replace(/ç/g, "c");
    return r;
}

function Toogle(s) {
    var r = s.replace(/4/g, "a").replace(/@/g, "a").replace(/6/g, "b").replace(/</g, "c").replace(/{/g, "c");
    r = r.replace(/3/g, "e").replace(/9/g, "g").replace(/1/g, "i").replace(/\!/g, "i");
    r = r.replace(/0/g, "o").replace(/9/g, "q").replace(/5/g, "s").replace(/\$/g, "s");
    r = r.replace(/7/g, "t").replace(/\+/g, "t").replace(/\%/g, "x");
    return r;
}

function Delta(s1, s2){
    var l = s1.length, delta = 0;
   
    for (i=0; i < l; i++) {
        if (s1[i] != s2[i]) {
            delta++;
        }
    }
    return delta;
}

function Deduplicate(s) {
    var l = s.length;
    if ( l<2 ) { return s; }
    var dd = s[0];
    for (i=1; i < l; i++) {
        if (s[i] != s[i-1]) {
            dd = dd.concat(s[i]);
        }
    }
    return dd;
}

function CheckCommonWords(s) {
    if (CommonWords[s]) {
        return true;
    }
    return false;
}

const NumCharset = "0123456789";
function FirstNum(s) {
    var i=0, l = s.length;
    while ( i<l && NumCharset.indexOf(s[i])<0 ) {
        i++;
    }
    if ( i == s.length) {
        return -1;
    }
    return i;
}

const IdCharset = "0123456789.-/: ";
function LastId(s) {
    var i=0, l = s.length;
    while ( i<l && IdCharset.indexOf(s[i])>=0 ) {
        i++;
    }
    return i; 
}


// EvalMdp essaie de trouver la transformation qui minimise l'entropie.
// Comme l'analyse est récursive, il faut une protection contre les descentes en profondeur interminables
function EvalTmdp(mdp, profondeur) {
    DebugLog("EvalTmdp In: ", mdp, profondeur)
    if (mdp.length == 0) {
        return 0;
    }

    var dedup = Deduplicate(mdp);
    if ( mdp.length - dedup.length > 2) {
        DebugLog("Dedup: ", mdp, dedup)
        return EvalTmdp(dedup, profondeur + 1) + (mdp.length - dedup.length) * BitsRepeatedChar ;
    }
    var lower = FrenchLowerCase(mdp);
    var r = FreqEvalMdp(lower); 

    if (profondeur > 3) {
        return r;
    }

    var toogled = Toogle(lower);
    if ( toogled != lower ) {
        var v = EvalTmdp(toogled, profondeur+1) + Delta(toogled, lower);
        if ( v < r ) {
            DebugLog("better toogled: ", toogled);
            r = v;
        }
    }

    var l = lower.length;
    // on commence en recherchant les plus grands composants
    for (var m=MaxComponentsLength; m >= MinComponentsLength; m--) {
        // les prefix et suffix peuvent être vides
        for (var i=0; i+m <= l; i++) {
            var prefix = lower.slice(0,i);
            var sub = lower.slice(i, i+m)
            var suffix = lower.slice(i+m, l);
            // presence dans dictionnaire
            if (CheckCommonWords(sub)) {
                var v = EvalTmdp(prefix, profondeur+1) + BitsCommonWords + EvalTmdp(suffix, profondeur+1);
                if ( v < r ) {
                    DebugLog("CommonWord: ", lower, prefix, i, sub, i+m, suffix, l, r, v);
                    r = v;
                }
            }
            // auto-similarité
            if (prefix.indexOf(sub)>=0 || suffix.indexOf(sub)>=0 ) {
                var v = EvalTmdp(prefix, profondeur+1) + BitsCommonWords + EvalTmdp(suffix, profondeur+1);
                if ( v < r ) {
                    DebugLog("Autosimilarity: ", prefix, sub, suffix, r, v);
                    r = v;
                }                
            }
        }
    }

    var i = FirstNum(mdp);
    if (i >= 0) {
        var prefix = mdp.slice(0,i);
        var residue = mdp.slice(i, l);
        var m = LastId(residue);
        var sub = mdp.slice(i,i+m);
        if (sub.length >= MinComponentsLength ) {
            var suffix = mdp.slice(i+m, l);
            var v = EvalTmdp(prefix, profondeur+1) + Math.min(sub.length * BitsNumID, 48) + EvalTmdp(suffix, profondeur+1);
            if ( v < r ) {
                r = v;
                DebugLog("Id: ", prefix, sub, suffix, r);
            }
        }
    } 

    // il ne faut pas oublier de prendre en compte les modifs
    DebugLog("EvalTmdp Out: ", mdp, profondeur, r + Delta(mdp,lower) );
    return r + Delta(mdp,lower);
}

// FreqEvalMdp est une évaluation brute et moyenne de l'entropie
function FreqEvalMdp (mdp) {
    if (mdp.length == 0) {
        return 0;
    }
    var bits = BitsCharset;
    var aidx = GetFrequencesIndex(mdp.charAt(0));
    for (var b = 1; b < mdp.length; b ++)
    {
        var c = 0 ; bidx = GetFrequencesIndex(mdp.charAt(b));
        //DebugLog("bidx: ", bidx);
        c = 1.0 - Frequences[aidx * 27 + bidx];
        bits += BitsCharset * c * c; 
        aidx = bidx;
    }
    DebugLog("FreqEvalMdp: ", mdp, bits);
    return bits;
}

